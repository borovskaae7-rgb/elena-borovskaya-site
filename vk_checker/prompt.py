from __future__ import annotations

import json
from vk_checker.models import CommunityData

CATEGORIES = [
    "Онлайн-школа", "Эксперт", "Коуч", "Наставник", "Психолог", "Продюсер", "Маркетинг",
    "SMM", "Таргет", "Маркетплейсы", "Инвестиции", "Финансы", "Недвижимость", "Языковая школа",
    "Детское образование", "Подготовка к ЕГЭ", "Бизнес", "Фриланс", "Дизайн", "IT",
    "Программирование", "Нутрициология", "Фитнес", "Красота", "Медицина", "Другое", "Не относится",
]

SYSTEM_PROMPT = """Ты классифицируешь сообщества ВКонтакте. Отвечай только валидным JSON без Markdown.
Определи, относится ли сообщество к инфобизнесу, онлайн-образованию, экспертным услугам или продаже знаний.
Учитывай курсы, вебинары, интенсивы, марафоны, наставничество, консультации, онлайн-школы и образовательные проекты.
Если данных недостаточно или сообщество не продаёт знания/обучение, ставь is_infobiz=false и category='Не относится'."""


def build_user_prompt(data: CommunityData) -> str:
    payload = data.model_dump(mode="json")
    return (
        "Данные сообщества VK:\n"
        f"{json.dumps(payload, ensure_ascii=False, indent=2)}\n\n"
        f"Допустимые категории: {', '.join(CATEGORIES)}.\n"
        "Верни строго JSON формата: "
        '{"is_infobiz": true, "category": "...", "confidence": 0.98, '
        '"reason": "...", "subcategory": "...", "offers": ["..."]}. '
        "offers должен перечислять найденные признаки/офферы: курсы, консультации, наставничество, вебинары, марафоны, интенсивы и т.п."
    )
